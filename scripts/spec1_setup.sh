install_if_missing() {
    local pkg=$1
    local cmd=${2:-$1}
    if ! command -v "$cmd" &>/dev/null; then
        warn "$pkg not found — installing..."
        sudo apt-get install -y "$pkg" &>/dev/null && success "Installed $pkg" || fail "Could not install $pkg"
    else
        success "$pkg already installed"
    fi
}

