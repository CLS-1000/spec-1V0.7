alias spec1-matrix='python3 ~/spec-1/scripts/harvest_index.py --print'

sdomain() { rg "@domain:.*$1" --glob "*.py" --glob "*.sh" -l; }
sstatus() { rg "@status:.*$1" --glob "*.py" --glob "*.sh" -l; }
sloc()    { rg "@loc:.*$1"    --glob "*.py" --glob "*.sh" -l; }
# ─────────────────────────────────────────────────────────────
ALIASEOF
    success "Aliases written to $SHELL_RC"
fi

